    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Password1 = jPasswordField1.getText();
        String Password2 = jPasswordField2.getText();
        JSONObject jsonObjectPassword = new JSONObject();
        UsermangementClient client = new UsermangementClient(); 
        
        if(Password1.equals(Password2))
        {
            try {
                jsonObjectPassword.put("username","admin");
                jsonObjectPassword.put("password",client.encrypt(Password2));
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(Password_Reset.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.out.println("jsonObjectPassword = "+jsonObjectPassword);
            client.sendData("http://localhost:8080/api/usrmgtservice/passwordreset", jsonObjectPassword);
            
            VergerMain vergermain = new VergerMain("admin");
            vergermain.setVisible(true);
            this.dispose();
        }
        else{
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            lbl_validation.setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

